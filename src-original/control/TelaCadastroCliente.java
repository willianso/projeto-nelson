    private void adicionar(){
        String sql = "insert into clientes(nome,endereco,cidade,estado,cep,telefone,email) values (?,?,?,?,?,?,?)";
        try{
            pst = conexao.prepareStatement(sql);
            pst.setString(1,txtNome.getText());
            pst.setString(2,txtEndereco.getText());
            pst.setString(3,txtCidade.getText());
            pst.setString(4,txtEstado.getText());
            pst.setString(5,txtCep.getText());
            pst.setString(6,txtTelefone.getText());
            pst.setString(7,txtEmail.getText());
            
            if(txtNome.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha o campo nome!");
            }
            else{
                int adicionado = pst.executeUpdate();
                if(adicionado > 0){
                    JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
                    txtNome.setText(null);
                    txtEndereco.setText(null);
                    txtCidade.setText(null);
                    txtEstado.setText(null);
                    txtCep.setText(null);
                    txtTelefone.setText(null);
                    txtEmail.setText(null);
                }
            }            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }

